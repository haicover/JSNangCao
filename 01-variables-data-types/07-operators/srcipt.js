// 1. Arithmetic Operators:Toán tử số học

let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5;
console.log(x);
// Concatenation:Nối
x = 'Hello' + ' ' + 'World';
console.log(x);
// Exponent:Số mũ
x = 2 ** 3;
console.log(x);
// Increment:Tăng
x = 1;
// x = x + 1;
x++;
console.log(x);
// Decrement:Giảm
// x = x - 1;
x--;
console.log(x);
// 2. Assignment Operators:Toán tử gán

x = 10;

x += 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;
x **= 5;
console.log(x);
// 3. Comparison Operators:Toán tử so sánh

// Equal to (Just the value, not the type):Bằng (Chỉ giá trị, không phải loại)
x = 2 == '2';
console.log(x);
// Equal to (Type and value):Bằng (Loại và giá trị)
x = 2 === '2';
console.log(x);
// Not equal to (Just the value, not the type):Không bằng (Chỉ giá trị, không phải loại)
x = 2 != '2';
console.log(x);
// Not equal to (Type and value):Không bằng (Loại và giá trị)
x = 2 !== 2;
console.log(x);
// Greater than and less than:Lớn hơn và nhỏ hơn
x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;

console.log(x);