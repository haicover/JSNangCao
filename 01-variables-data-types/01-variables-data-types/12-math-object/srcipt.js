let x;

// Square root
x = Math.sqrt(9);
console.log("1:", x);
// Absolute value
x = Math.abs(-5);
console.log("2:", x);
// Round
x = Math.round(4.2);
console.log("3:", x);
// Round up
x = Math.ceil(4.2);
console.log("4:", x);
// Round down
x = Math.floor(4.9);
console.log("5:", x);
// Exponent
x = Math.pow(2, 3);
console.log("6:", x);
// Minimum number
x = Math.min(4, 5, 3);
console.log("7:", x);
// Maximum number
x = Math.max(4, 5, 3);
console.log("8:", x);
// Get a random number/decimal between 0 and 1
x = Math.random();
console.log("9:", x);
// Get a random number between 1 and 100
x = Math.floor(Math.random() * 100 + 1);

console.log(x);